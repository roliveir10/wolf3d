#include "libft.h"

void				ft_tab_reverse(char ***tab)
{
	int				i;
	int				j;

	i = -1;
	j = 0;
	while (*tab[++i])
		j++;
	i = -1;
	while (++i < --j)
		ft_swap(tab[i], tab[j]);
}
